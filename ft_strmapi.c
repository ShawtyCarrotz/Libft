#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*string;
	unsigned int	stringlen;
	unsigned int	i;

	stringlen = ft_strlen(s);
	string = malloc(sizeof(char) * stringlen + 1);
	if (string == NULL)
		return (NULL);
	i = 0;
	while (s[i])
	{
		string[i] = f(i, s[i]);
		i++;
	}
	string[i] = '\0';
	return (string);
}
